#include "math_functions.h"

double runAddition(double x, double y) {
    double result = x + y;
    return result;
}

double runSubstraction(double x, double y) {
    double result = x - y;
    return result;
}

double runMultiplication(double x, double y) {
    double result = x * y;
    return result;
}

double runDivision(double x, double y) {
    double result = x / y;
    return result;
}

double runPotency(double x, double y) {
    double result = pow(x, y);
    return result;
}

double runSquareRot(double x) {
    double result = sqrt(x);
    return result;
}

double runReverse(double x) {
    double result = (-(x));
    return result;
}

double runDivisonWithOne(double x) {
    double result = (1 / x);
    return result;
}